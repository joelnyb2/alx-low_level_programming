#include "3-calc.h"
/**
 * op_add - adds two number
 * @a: number 1
 * @b: number 2
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtracts two numbers
 * @a: number 1
 * @b: number 2
 * Return: difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplies numbers
 * @a: number1
 * @b: number2
 * Return: multiplication
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divides number
 * @a: number 1
 * @b: number 2
 * Return: division
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modulus of numbers
 * @a: number 1
 * @b: number 2
 * Return: modulus
 */
int op_mod(int a, int b)
{
	return (a % b);
}
